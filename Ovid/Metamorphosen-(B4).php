          dum redit in silvas, inventos forte sine ipsa
          ore cruentato tenues laniavit amictus.
        german: |
          Nachdem sie die Tür geöffnet hatte, ging die schlaue Thisbe
          in die Dunkelheit hinaus, täuschte die ihren, gelangte mit verhülltem
          Gesicht zum Hügel und setzte sich unter den vereinbarten Baum.
          Die Liebe machte sie mutig. Schau, da kommt eine Löwin mit schäumendem Maul,
          das noch besudelt ist vom frischen Blut eines kürzlich getöteten Rindes,
          um ihren Durst im Wasser der benachbarten Quelle zu stillen.
          Die babylonische Thisbe sah diese von der Ferne im Mondlicht,
          floh ängstlichen Fußes in die dunkle Höhle und verlor ihren Schleier,
          der ihr vom Rücken geglitten war.
          Als die wilde Löwin ihren Durst mit viel Wasser gestillt hatte,
          zerfetzte sie mit blutbespritztem Maul den dünnen Umhang,
          den sie zufällig und ohne Thisbe selbst gefunden hatte, während sie in die Wälder zurückkehrte.
