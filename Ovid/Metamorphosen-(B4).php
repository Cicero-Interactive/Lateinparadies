        latin: |
          Pyramus et Thisbe, iuvenum pulcherrimus alter,
          altera, quas Oriens habuit, praelata puellis,
          contiguas tenuere domos, ubi dicitur altam
          coctilibus muris cinxisse Semiramis urbem.
          notitiam primosque gradus vicinia fecit,
          tempore crevit amor; taedae quoque iure coissent,
          sed vetuere patres: quod non potuere vetare,
          ex aequo captis ardebant mentibus ambo.
          conscius omnis abest; nutu signisque loquuntur,
          quoque magis tegitur, tectus magis aestuat ignis.
          fissus erat tenui rima, quam duxerat olim,
          cum fieret, paries domui communis utrique.
          id vitium nulli per saecula longa notatum -
          quid non sentit amor? - primi vidistis amantes
          et vocis fecistis iter, tutaeque per illud
          murmure blanditiae minimo transire solebant.
          saepe, ubi constiterant hinc Thisbe, Pyramus illinc,
          inque vices fuerat captatus anhelitus oris,
          "invide" dicebant "paries, quid amantibus obstas?
          quantum erat, ut sineres toto nos corpore iungi
          aut, hoc si nimium est, vel ad oscula danda pateres?
          nec sumus ingrati: tibi nos debere fatemur,
          quod datus est verbis ad amicas transitus auris."
        german: |
          Pyramus und Thisbe, der eine der schönste Jüngling,
          die andere vorgezogen <span class="annotation">[sogar]</span> den Mädchen, die der Orient hatte,
          bewohnten benachbarte Häuser, wo Semiramis die hohe Stadt
          mit Mauern aus Tonziegeln errichtet haben soll.
          Die Nachbarschaft bewirkte <span class="annotation">[deren]</span> Bekanntschaft und erste Schritte,
          und mit der Zeit wuchs die Liebe. Auch die Hochzeitsfackeln hätten mit Recht zusammengehen können,
          aber die Väter haben es verboten. Was sie nicht verbieten konnten:
          Beide brannten gleichermaßen, nachdem ihre Sinne <span class="annotation">[von der Liebe]</span> erfasst worden waren.
          Jeder Mitwisser war abwesend; sie sprachen durch Zunicken und Zeichen,
          und je mehr das Feuer bedeckt wurde, desto mehr loderte es auf.
          Gespalten war die gemeinsame Trennwand beider Häuser von einem dünnen Ritz,
          der einst beim Bau der Mauer entstanden war.
          Diesen über lange Jahrhunderte von niemandem bemerkten Fehler habt - 
          was bemerkt die Liebe nicht? - als erste ihr Liebenden gesehen
          und habt ihn zu einem Weg für eure Stimme gemacht;
          und sicher pflegten die Schmeicheleien in Form von leisestem Gemurmel durch jenen hinüberzugehen.
          Oft, sobald sie dastanden, hier Thisbe, dort Pyramus,
          und gegenseitig der Atem des Mundes gefangen worden war,
          sagten sie: „Neidische Mauer, was hinderst Du die Liebenden?
          Wie viel wäre schon dabei, wenn du zulassen würdest, dass wir uns am ganzen Körper vereinen,
          oder, wenn das zu viel ist, du wenigstens zum Austausch von Küssen offenstehen würdest?
          Aber wir sind nicht undankbar; wir geben zu, dass wir dir Dank schulden,
          weil du den Worten einen Zugang zu den befreundeten Ohren geschaffen hast."
