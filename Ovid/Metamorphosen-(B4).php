        german: |
          Nachdem sie so vergeblich auf getrennten Plätzen sitzend gesprochen hatten,
          sagten sie bei Einbruch der Nacht „Auf Wiedersehen“ und gaben ein jeder
          seinen Teil Küsse, die nicht auf die Gegenseite gelangten.
          Das folgende Sonnenlicht hatte die nächtlichen Feuer entfernt,
          und die Sonne hatte die taufeuchten Kräuter mit Sonnenstrahlen getrocknet.
          Sie trafen sich am gewohnten Platz. Dann beschlossen sie mit leisem Geflüster,
          nachdem sie sich zuvor über vieles beklagt hatten, dass sie versuchen würden,
          in der nächtlichen Stille die Wächter zu täuschen und aus dem Haus hinauszugehen,
          dass sie auch die Dächer der Stadt verlassen, wenn sie aus dem Haus gehen.
