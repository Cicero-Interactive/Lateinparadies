																	Ich hoffe, dass ich in meinen Büchern einem solchen Mittelweg gefolgt bin, dass sich niemand über diese beklagen kann,
																	der ein gutes Gewissen hat, da sie selbst gegenüber niedrigsten Personen mit allem Respekt scherzen. Dieser fehlte den alten Autoren so sehr,
																	dass sie nicht nur wirkliche, sondern auch große Namen missbrauchten. Mir möge der Ruhm für einen geringeren Preis zuteilwerden, und gelobt werden soll an mir zuallerletzt mein Talent.
																	Es möge von der Offenheit meiner Scherze ein böswilliger Erklärer fern bleiben und nicht meine Epigramme umschreiben: Unrecht handelt,
																	wer anhand eines fremden Buch Talent zeigt. Ich würde mich für die freizügige Wahrheit der Worte entschuldigen, das heißt für die Sprache der Epigramme,
																	wenn ich das Beispiel gegeben hätte: Aber so schreibt Catull, so Marsus, so Pedo, so Gaetulicus, so jeder, der gründlich gelesen wird.
																	Wenn dennoch irgendjemand so übertrieben ernst ist, dass es bei jenem auf keiner Seite erlaubt ist, offen zu reden,
																	kann er sich mit einem Brief oder besser noch mit dem Buchtitel begnügen. Epigramme werden für jene geschrieben,
