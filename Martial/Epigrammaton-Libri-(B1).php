      - latin: |
          Spero me secutum in libellis meis tale temperamentum ut de illis queri non possit quisquis de se bene senserit, cum salua infirmarum quoque personarum reuerentia ludant; quae adeo antiquis auctoribus defuit ut nominibus non tantum ueris abusi sint, sed et magnis. Mihi fama uilius constet et probetur in me nouissimum ingenium. Absit a iocorum nostrorum simplicitate malignus interpres nec epigrammata mea scribat: inprobe facit qui in alieno libro ingeniosus est. Lasciuam uerborum ueritatem, id est epigrammaton linguam, excusarem, si meum esset exemplum: sic scribit Catullus, sic Marsus, sic Pedo, sic Gaetulicus, sic quicumque perlegitur. Si quis tamen tam ambitiose tristis est ut apud illum in nulla pagina latine loqui fas sit, potest epistola uel potius titulo contentus esse. Epigrammata illis scribuntur qui solent spectare Florales. Non intret Cato theatrum meum, aut si intrauerit, spectet. Videor mihi meo iure facturus si epistolam uersibus clusero:
        german: |
          Ich hoffe, dass ich in meinen Büchern einem solchen Mittelweg gefolgt bin, dass sich niemand über diese beklagen kann, der ein gutes Gewissen hat, da sie selbst gegenüber niedrigsten Personen mit allem Respekt scherzen. Dieser fehlte den alten Autoren so sehr, dass sie nicht nur wirkliche, sondern auch große Namen missbrauchten. Mir möge der Ruhm für einen geringeren Preis zuteilwerden, und gelobt w zuallerletzt mein Talent. Es möge von der Offenheit meiner Scherze ein böswilliger Erklärer fern bleiben und nicht meine Epigramme umschreiben: Unrecht handelt, wer anhand eines fremden Buch Talent zeigt. Ich würde mich für die freizügige Wahrheit der Worte entschuldigen, das heißt für die Sprache der Epigramme, wenn ich das Beispiel gegeben hätte: Aber so schreibt Catull, so Marsus, so Pedo, so Gaetulicus, so jeder, der gründlich gelesen wird. Wenn dennoch irgendjemand so übertrieben ernst ist, dass es bei jenem auf keiner Seite erlaubt ist, offen zu reden, kann er sich mit einem Brief oder besser noch mit dem Buchtitel begnügen. Epigramme werden für jene geschrieben, die Zuschauer beim Florafest zu sein pflegen <span class="annotation">(die gewohnt sind, Nackttänzerinnen zu betrachten)</span>. Kein Cato möge mein Theater betreten, oder, wenn er es denn betritt, möge er zuschauen. Ich glaube, dass ich rechtmäßig handle, wenn ich den Brief mit diesen Versen beende:  </span>
