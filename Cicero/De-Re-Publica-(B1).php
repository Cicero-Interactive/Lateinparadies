      Jede dieser drei Verfassungsformen - wenn die Menschen jenes Band zusammenhält, das sie zuerst unter sich gemeinschaftlich zu einem Staat verbunden hat - ist freilich nicht perfekt und nicht die Beste meiner Meinung nach ist, jedoch erträglich; und bei jeder Verfassungsform ist es so, dass die eine vortrefflicher sein könnte als die andere. Denn entweder ein gerechter und weiser König oder die Auserwählten und führenden Bürger oder das Volk selbst, obwohl dies am wenigsten zu billigen ist, diese Staatsformen scheinen dennoch von einigermaßen sicheren Zustand zu sein,wenn keine Ungerechtigkeiten und Begierden dazwischenkommen. Aber in den Monarchien haben die Übrigen allzu wenig Anteil am gemeinsamen Recht und der Entscheidung; und bei der Herrschaft der Optimaten kann die Menge kaum Anteil an Freiheit haben, weil sie jegliche gemeinsame Planung und Macht entbehrt; und wenn alles vom noch so gerechten und gemäßigten Volk ausgeführt wird, ist dennoch die Gleichheit selbst ungleich, dadurch, dass sie keine Abstufungen der Würde hat.
