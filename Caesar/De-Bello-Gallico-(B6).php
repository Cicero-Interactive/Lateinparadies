														Kaum jemand besitzt bei ihnen ein bestimmtes Maß an Feldern oder eigene Gebiete, sondern Beamte und führende Maenner teilen jaehrlich den Familien und Verwandten Felder zu,
														im naechsten Jahr zwingen sie sie, woanders hinzuziehen.
														Dafür führen sie viele Gründe an: damit sie nicht das Streben nach Kriegsführung gegen Ackerbau eintauschen; damit sie sich nicht bemühen,
														weite Gebiete zu erwerben und die Maechtigeren nicht die Niedrigeren aus ihren Besitztümern vertreiben; damit sie nicht allzu sorgfaeltig bauen zum Meiden von Kaelte und Hitze;
														damit keine Geldgier entsteht, woraus Verfeindungen und Aufstaende entstehen; und damit das Volk durch Gelassenheit zusammenhaelt, weil jeder sieht,
														dass seine Reichtümer mit denen der Maechtigsten gleichgestellt werden.
													</font>
