														Die Germanen benutzen jetzt <font color="#CCCCCC">[immer noch]</font> dieselbe Lebensweise und Körperpflege,
														da sie ja auf demselben Mangel und derselben Dürftigkeit und Genügsamkeit verharren.
														Den Galliern aber verschafft die Naehe römischer Provinzen und die Bekanntschaft mit den über das Meer kommenden Waren mehr zum eigenen Wohlstand und Luxus.
														Allmaehlich sind sie daran gewöhnt besiegt zu werden und vergleichen sich nach vielen Niederlagen nicht einmal mehr selbst an Tapferkeit mit den Germanen.
