														Es gibt ebenso Tiere, die Elche genannt werden. Deren Gestalt und Verschiedenartigkeit der Felle ist den Ziegen aehnlich, aber an Größe übertreffen sie sie ein wenig.
														An den Hörnern sind sie verstümmelt und sie haben Unterschenkel ohne Knöchel und Gelenke. Weder legen sie sich zur Ruhe hin noch können sie,
														wenn sie durch irgendeinen Zufall umgeworfen wurden, sich wieder aufrichten oder aufstehen. Ihnen dienen Baeume als Schlafstaetten.
														Sie lehnen sich an ihnen an und fassen so, nur ein wenig rückwaerts an diesen angelehnt, Ruhe.
														Wenn Jaeger durch Fußspuren bemerkt haben, wohin sie sich gewöhnlich zurückziehen, untergraben sie dort alle Baeume oder kerben sie sie gerade so sehr ein,
														dass im Ganzen noch der Eindruck stehender Baeume bleibt.
														Wenn die Elche sich ihrer Gewohnheit nach hier angelehnt haben, bringen sie die schwachen Baeume durch ihr Gewicht zu Fall und werden dann selbst getötet.
