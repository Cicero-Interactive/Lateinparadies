													<font size="4" color="#F2F2F2">
														Das ganze Volk der Gallier ist völlig der Götterverehrung hingegeben, deswegen opfern die, die von schwereren Krankheiten befallen sind und diese,
														die sich in Schlachten und Gefahren aufhalten, entweder an Stelle der Opfertiere Menschen, oder sie schwören, dass sie sie opfern werden;
														und sie verwenden Druiden als Helfer für diese Opfer, weil sie glauben, dass der Wille der unsterblichen Götter nicht besaenftigt werden kann,
														wenn nicht für das Leben eines Menschen das eines anderen zrückgegeben wird, und sie haben öffentlich eingerichtetet Opferhandlungen dieser Art.
														Einige Staemme haben riesig große Götzenbilder aus Weidengeflecht, deren Glieder sie mit lebenden Menschen anfüllen;
														diese werden dann von unten nach oben angezündet und so die Unglücklichen dem Feuertod geweiht.
														Sie glauben, dass die Hinrichtung derer, die in einem Diebstahl, Raub oder einem anderen Vergehen ergriffen wurden, den unsterblichen Göttern willkommener seien;
														wenn aber die Menge dieser Art fehlt, steigen sie auch herab zu Hinrichtungen Unschuldiger.
													</font>
												</p>
											</td>
