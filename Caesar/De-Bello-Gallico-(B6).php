														Und so besetzten die Tektosagen, ein Stamm der Volker, die Orte, die die fruchtbarsten Germaniens um den Herkynischen Wald herum sind,
														der, wie ich sehe, schon Eratosthenes und gewissen <font color="#CCCCCC">[anderen]</font> Griechen unter dem Namen Orkynischer Wald durch Hörensagen bekannt war,
