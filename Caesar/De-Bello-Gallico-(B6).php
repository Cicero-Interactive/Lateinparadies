														ihrem Aussehen, ihrer Farbe und Gestalt nach sind sie Stiere. Sie haben große Kraft und Schnelligkeit; sie schonen weder Mensch noch Tier, das sie erblickt haben.
														Nachdem sie eifrig in Gruben gefangen wurden, töten die Germanen sie. Durch diese Arbeit haerten sich die jungen Maenner ab und tainieren durch diese Art der Jagd.
														Diejenigen, die am meisten von diesen getötet haben, bekommen großes Lob, nachdem die als Beweis dienenden Hörner in der öffentlichkeit verkündet wurden.
														Nicht einmal die sehr kleinen Auerochsen, die aus der Wildnis eingefangen wurden, können sich an die Menschen gewöhnen und zahm werden.
														Die Weite, Gestalt und das Aussehen ihrer Hörner unterscheiden sich sehr von den Hörnern unserer Ochsen.
														Die Germanen umschließen diese Hörner, die eifrig zusammengesucht worden sind, am Rand mit Silber und verwenden sie bei sehr praechtigen Festmaehlern an Stelle von Bechern.
