											<td>
												<p>
													<font size="4" color="#F2F2F2">
														Civitatibus maxima laus est quam latissime circum se vastatis finibus solitudines habere.
														Hoc proprium virtutis existimant, expulsos agris finitimos cedere, neque quemquam prope audere consistere;
														simul hoc se fore tutiores arbitrantur repentinae incursionis timore sublato. Cum bellum civitas aut illa tum defendit aut infert,
														magistratus, qui ei bello praesint, ut vitae necisque habeant potestatem, deliguntur. In pace nullus est communis magistratus,
														sed principes regionum atque pagorum inter suos ius dicunt controversiasque minuunt. 
														Latrocinia nullam habent infamiam, quae extra fines cuiusque civitatis fiunt, atque ea iuventutis exercendae ac desidiae minuendae causa fieri praedicant. 
														Atque ubi quis ex principibus in concilio dixit se ducem fore, qui sequi velint, profiteantur,
														consurgunt ei qui et causam et hominem probant suumque auxilium pollicentur atque ab multitudine collaudantur:
														qui ex his secuti non sunt, in desertorum ac proditorum numero ducuntur, omniumque his rerum postea fides derogatur.
														Hospitem violare fas non putant; qui quacumque de causa ad eos venerunt, ab iniuria prohibent, sanctos habent, hisque omnium domus patent victusque communicatur. 
													</font>
												</p>
											</td>
											<td></td>
											<td>
												<p>
													<font size="4" color="#F2F2F2">
														Für die Staemme ist es das größte Lob, nach Verwüstung des Gebiets möglichst weit um sich herum Einsamkeit zu haben.
														Sie halten es für ein Kennzeichen von Tapferkeit, dass die aus ihren Feldern vertriebenen Nachbarn weichen und keiner es wagt, sich in ihrer Naehe niederzulassen.
														Gleichzeitig glauben sie, dass sie dadurch sicherer sein würden, nachdem die Angst vor einem plötzlichen Angriff aufgehoben worden ist.
														Wenn ein Stamm einen Angriffskrieg entweder verteidigt oder selbst beginnt, werden Beamte gewaehlt, die diesem Krieg vorstehen und Macht über Leben und Tod haben.
														Im Frieden gibt es keine Beamte, sondern führende Maenner aus Gebieten und Dörfern sprechen untereinenader Recht und verringern Streitigkeiten.
														Raubzüge gelten als nicht schaendlich, wenn sie außerhalb des eigenen Stammesgebiets geschehen; sie rühmen sie sogar als Mittel gegen den Müßiggang und zur Ertüchtigung der Jugend.
														Sobald irgendeiner der führenden Maenner in der Versammlung sagt, dass er Anführer sein wolle und diejenigen, die ihm folgen wollen, sich dazu bekennen sollen,
														so erheben sich alle, die sowohl die Sache als auch den Mann gut finden und versprechen ihm unter lautem Beifall der Menge ihre Unterstützung.
														Jene, die ihm aber spaeter dennoch nicht folgen, werden zu den Fahnenflüchtigen und Verraetern gerechnet, und das Vertrauen in allen Dingen wird ihnen spaeter entzogen.
														Sie halten es für ein No-Go <font color="#CCCCCC">(wörtlich: "nicht für ein göttliches Recht")</font>, einen Gast zu verletzen;
														wenn einer aus welchem Grund auch immer zu ihnen gekommen ist, so schaetzen sie ihn als unverletzlich gegen jede Beleidigung,
														die Haeuser aller stehen ihm offen und die Nahrung wird zu einer gemeinschaftlichen gemacht.
													</font>
												</p>
											</td>
