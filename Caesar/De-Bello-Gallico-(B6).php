												<p>
													<font size="4" color="#F2F2F2">
														Germani multum ab hac consuetudine differunt. Nam neque druides habent, qui rebus divinis praesint, neque sacrificiis student.
														Deorum numero eos solos ducunt, quos cernunt et quorum aperte opibus iuvantur, Solem et Vulcanum et Lunam, reliquos ne fama quidem acceperunt.
														Vita omnis in venationibus atque in studiis rei militaris consistit: ab parvulis labori ac duritiae student. Qui diutissime impuberes permanserunt,
														maximam inter suos ferunt laudem: hoc ali staturam, ali vires nervosque confirmari putant.
														Intra annum vero vicesimum feminae notitiam habuisse in turpissimis habent rebus; cuius rei nulla est occultatio, quod et promiscue in fluminibus
														perluuntur et pellibus aut parvis renonum tegimentis utuntur magna corporis parte nuda.
													</font>
												</p>
											</td>
											<td></td>
											<td>
												<p>
													<font size="4" color="#F2F2F2">
														Die Germanen unterscheiden sich viel von der Gewohnheit der Gallier. Denn sie haben weder Druiden, die den göttlichen Angelegenheiten vorstehen,
														noch bemühen sie sich um Opfer. Zu den Göttern rechnen sie nur die, die sie sehen und durch deren Macht sie offenkundig unterstützt werden;
														die Sonne, den Vulkan und den Mond. Von den übrigen Göttern haben sie nicht einmal durch Hörensagen erfahren. Ihr ganzes Leben besteht aus Jagden
														und aus den Bemühungen um das Kriegswesen; von klein auf bemühen sie sich um Arbeit und Abhaertung. Jenen, die am laengsten ohne sexuellen Kontakt bleiben,
														bringen sie das größte Lob entgegen: Sie glauben, dass dadurch der Körperbau und die Kraefte gefördert und die Muskeln gestaerkt werden.
														Dagegen rechnen sie es aber zu den schaendlichsten Dingen, vor dem 20. Lebensjahr sexuellen Kontakt mit einer Frau gehabt zu haben.
														Trotzdem gibt es in dieser Hinsicht keine Heimlichtuereien, weil sie sich sowohl gemeinsam in Flüssen waschen als auch Felle oder kleine Felzbedeckungen nutzen,
														wobei ein großer Teil des Körpers nackt ist.
													</font>
												</p>
											</td>
										</tr>
