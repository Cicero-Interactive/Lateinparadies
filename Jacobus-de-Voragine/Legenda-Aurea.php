													<font size="4" color="#F2F2F2">
														Cum vero ad specum a Romani monasterio iter
														non esset, panem in longissimo fune ligabat et
														sic eum deponere consuevat. Tintinnabulum
														etiam ipsi chordae inseruit, ut ad eius sonitum
														vir Dei cognosceret, quando sibi Romanus panem
														praeberet, quem exiens acciperet. Sed antiquus
														hostis [...] lapidem iactavit et tintinnabulum
														fregit, sed tamen Romanus eidem ministrare non
														desit.
														Moxque diabolus quandam feminam, quam aliquando
														viderat, ante eius mentis oculos reduxit eisque
														animum tanto igne in speciem illius accendit, ut
														paene heremum voluptate victus deserere deliberaret.
														Sed subito divina gratia ad se reversus continuo
														se exuit et in spinis et vepribus, quae ibi aderant,
														nudum se adeo volutavit, ut toto corpore inde
														vulneratus exiret et per cutis vulnera mentis
														vulnera educeret. Vicit itaque peccatum, quia
														mutavit incendium. Ab illo autem tempore nulla
														postmodum temptatio in eius corpore pullulavit. [...]
													</font>
												</p>
											</td>
