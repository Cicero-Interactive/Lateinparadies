												<p>
													<font size="4" color="#F2F2F2">
														Weil aber vom Kloster des Romanus zur Höhle kein Weg war,
														band er das Brot an ein langes Seil und hatte sich daran gewöhnt,
														es so abzulegen. Auch hat er ein Glöckchen an den Strick gebunden,
														sodass der Mann Gottes bei dessen Ton erkannte, wann ihm Romanus das Brot gab,
														welches er herausgehend annahm. Aber der alte Feind <font color="#CCCCCC">(der Teufel)</font> [...]
														warf einen Stein und zerbrach das Glöckchen. Dennoch hörte Romanus nicht auf,
														ihm zu dienen. Und bald zog der Teufel eine gewisse Frau,
														die Benedikt irgendwann einmal gesehen hatte, vor dessen <font color="#CCCCCC">[geistigen]</font>
														Augen zurück und entzündete dessen Herz mit einem so großen Feuer beim Anblick jener,
														dass er fast überlegt hätte, von der Lust besiegt die Einsamkeit zu verlassen.
														Aber plötzlich zog er sich durch die in ihn zurückgekehrte göttliche Gnade
														sofort aus und wälzte sich nackt in den sich dort befindenden Disteln und Dornsträuchern so sehr,
														dass er am ganzen Körper verletzt von dort herausstieg und durch die Wunde der Haut
														merzte er die Wunde des Herzens aus. [...]
													</font>
												</p>
